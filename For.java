public class For {

    public static void main(String args[]) {

        System.out.println("Primeiro laço");
        for(byte num=1; num<=5; num++){
            System.out.println(num);
        }
        
        System.out.println("Segundo laço");
        for(byte num=6; num>=5; num--){
            System.out.println(num);
        }
    }
    
}
