public class DoWhile {

    public static void main(String args[]) {

        int num =4;

        while(num>4){

            System.out.println(num);
            num--;
        }//verifica e depois faz
        
        System.out.println();
        int num1 = 5;
        do{
            System.out.println(num1);
            num1--;
        }while(num1>5);//faz e depois verifica
    }
    
}
